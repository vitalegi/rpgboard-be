package it.vitalegi.rpgboard.be;

public class Account {
	String id;
	String name;

	public Account() {
		super();
	}

	public Account(String id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
